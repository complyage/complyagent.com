package publish

//||------------------------------------------------------------------------------------------------||
//|| Import
//||------------------------------------------------------------------------------------------------||

import (
	"fmt"
	"time"

	"github.com/complyage/base/verify"
)

//||------------------------------------------------------------------------------------------------||
//|| Starts the Face verification process
//||------------------------------------------------------------------------------------------------||

func AgentVerifyFaceStart(v verify.Verification) error {

	fmt.Println("AgentVerifyFaceStart: Starting ID verification process for UUID:", v.UUID)
	//||------------------------------------------------------------------------------------------------||
	//|| Create the Agent Request
	//||------------------------------------------------------------------------------------------------||

	request := AgentVerification{
		Level:      1,
		Process:    ProcessFacialAge,
		Identifier: v.UUID,
		Timestamp:  time.Now().UTC().Format(time.RFC3339),
	}

	//||------------------------------------------------------------------------------------------------||
	//|| Send the request to RabbitMQ
	//||------------------------------------------------------------------------------------------------||

	pubErr := PublishAgentVerification(request)
	if pubErr != nil {
		return pubErr
	}

	//||------------------------------------------------------------------------------------------------||
	//|| Success
	//||------------------------------------------------------------------------------------------------||

	return nil
}
